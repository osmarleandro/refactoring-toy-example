package org.felines;



public class Tiger extends Feline implements Animal{

	private int speed;
	private long force;

	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	private long getForce() {
		return force;
	}
	private void setForce(long force) {
		this.force = force;
	}
	
	
	

}
